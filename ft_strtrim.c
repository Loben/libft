#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char	*str;
	char	*str_new;
	size_t	len;
	size_t	counter;

	if (s == NULL)
		return (NULL);
	str = (char*)s;
	while (*str == ' ' || *str == '\t' || *str == '\n')
		str++;
	len = ft_strlen(str) - 1;
	if (ft_strlen(str) == 0)
		return (str_new = ft_strnew(0));
	while (str[len] == ' ' || str[len] == '\t' || str[len] == '\n')
		len--;
	if (!(str_new = ft_strnew(len)))
		return (NULL);
	counter = 0;
	while (counter <= len)
	{
		str_new[counter] = str[counter];
		counter++;
	}
	str_new[counter] = 0;
	return (str_new);
}
